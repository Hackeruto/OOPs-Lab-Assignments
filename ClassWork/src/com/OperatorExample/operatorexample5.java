package com.OperatorExample;

public class operatorexample5 {
    public static void main(String[] args) {
        int a = 12;
        a += 4;
        System.out.println(a);
        a -= 3;
        System.out.println(a);
        a *= 4;
        System.out.println(a);
        a /= 5;
        System.out.println(a);
    }
}
