package com.OperatorExample;

public class operatorexample6 {
    public static void main(String[] args) {
        int a = 10, b = 20;
        System.out.println("a == b = " + (a == b));
        System.out.println("a != b = " + (a != b));
        System.out.println("a > b = " + (a > b));
        System.out.println("a < b = " + (a < b));
        System.out.println("a >= b = " + (a >= b));
        System.out.println("a <= b = " + (a <= b));
    }
}

//a == b = false
//a != b = true
//a > b = false
//a < b = true
//a >= b = false
//a <= b = true
